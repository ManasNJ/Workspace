/*
Types of Copying data from one object to another object :
1) Shallow Copy
2) Deep Copy

Shallow Copy:
Copy one object data to another object with the help of compiler provided(internal) copy constructor/Assignment operator 
is called as Shallow Copy.

Deep Copy:
Copy one object data to another object with the help of user provided(External) copy constructor/Assignment operator 
is called as Deep Copy. 
*/
